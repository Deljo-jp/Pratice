//For , While , do While

//Do While

let i = 0;
do{
    console.log('value of i is:', i);
    i++;
}while(i <= 5);

//While LOOP

let j = 0;
while(j <= 5){
    console.log('value of i =', j);
    j++
}

//For LOOP

for (let i = 0; i <= 5; i++){
    console.log('Value of i:', i);
}

for (let i = 0; i <= 5; i++){
    console.log(`${i} * ${i} = ${i * i}`);
}

