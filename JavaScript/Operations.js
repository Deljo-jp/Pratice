// Operator

//Assignment Operator
let x = 10;
let y = 10;

//Arithematic Operator
// + , - , *  , / , % 
let z = x + y;
console.log(z);

console.log(x + y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);

//Comparision Operators

let abc = 10;
let xyz = '10';
console.log(abc > xyz);
console.log(abc < xyz);
console.log(abc >= xyz);

// == and ===

// == (compairs values but not datatype)
console.log(abc == xyz);

// === (compairs values and datatypes)

console.log(abc === xyz);

//Conditions or Decisions

let age = 21;
if (age >= 18) {
console.log('You are allowed to apply for license');
}
else {
console.log('Not allowed');
}